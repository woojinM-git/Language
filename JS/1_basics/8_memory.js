/**
 * 메모리란? 
 * 
 * CPU = 연산을 하는 역할
 * 
 * Memory = 데이터를 저장하는 공간
 * 
 * 메모리 셀(Memory Cell)
 * 
 * 메모리 셀 = 1bit(바이트)
 * 
 * 메모리 주소
 * 1GB = 1,073,731,824 bit
 * 
 * 우리는 표현을 할때 16진수로 표시함
 * 
 * 메모리 활용
 * let calc = 2+5 (7)
 * 
 * 메모리셀의 2와 5를 가져와서 연산하고 7(0x000006)로 만들어 다시 저장한다
 * 
 * 여기서 calc라는 변수명이 하는 역할은 메모리 셀의 calc라는 영역을 만든다. 
 */